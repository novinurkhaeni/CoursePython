# ● Operator aritmatika digunakan untuk melakukan operasi matematika.
# ● Terdapat beberapa jenis operator aritmatika
# ● Operator dasar, seperti + (tambah), - (kurang), * (kali), / (bagi)
# ● Operator pembagian bulat dan modulo, seperti // (pembagian bulat), % (modulo, 
# sisa pembagian)
# ● Operator pangkat, seperti ** (pangkat)

a = 10
b = 3

penjumlahan = a + b # 10 + 3 = 13
pengurangan = a - b # 10 - 3 = 7
perkalian = a * b # 10 * 3 = 30
pembagian = a / b # 10 / 3 = 3.3333
pembulatan = a // b # 10 / 3 = 3 (hasil tanpa desimal)
sisa_bagi = a % b #sisa bagi dari 10 / 3 adalah 1
pangkat = a ** b #10 pangkat 3 = 1000

print(penjumlahan)
print(pengurangan)
print(perkalian)
print(pembagian)
print(pembulatan)
print(sisa_bagi)
print(pangkat)
