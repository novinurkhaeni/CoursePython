# ● Operator assignment digunakan untuk memberikan nilai pada variabel, seperti 
# yang sudah pernah kita bahas, menggunakan tanda = (sama dengan)
# ● Compound Assignment, yaitu operator yang menggabungkan operasi aritmatika 
# dengan assignment

x = 10
print("x awal:", x)

x += 5
# sama dengan x = x + 5
print("x setelah += 5: ", x)

x -= 3
# sama dengan x = x - 3
print("x setelah -= 3: ", x)

x *= 2
# sama dengan x = x * 2
print("x setelah *= 2: ", x)

x /= 4
# sama dengan x = x / 4
print("x setelah /= 4: ", x)