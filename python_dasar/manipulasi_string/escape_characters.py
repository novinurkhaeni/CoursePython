#● Escape Characters merupakan karakter-karakter khusus dalam string, untuk
# menggunakan karakter khusus kita perlu menggunakan aturan tertentu
# ● \n (New Line)
# ● \t (Tab)
# ● \ (Backslash)
# ● " dan ' (Kutip dalam String), perlu menambahkan \ diawal

kalimat = "Baris pertama\nBaris kedua"
print(kalimat)
#Baris Pertama
#Baris kedua

data = "Nama:\tAlice\nUmur:\t25"
print(data)
#Nama:  Alice
#Umur:  25

path = "C:\\Users\\Alice\\Documents"
print(path) #"C:\Users\Alice\Documents

kalimat1 = "Dia berkata \"Hello\" kepada saya"
print(kalimat1) #"Dia berkata "Hello" kepada saya

kalimat2 = 'I\'m learning Python'
print(kalimat2) #I'm learning Python