# ● Integer (int) - Bilangan Bulat
# ● Float - Bilangan Desimal
# ● String (str) - Teks
# ● Boolean (bool) - True/False

# int bilangan bulat positif dan negatif
umur = 25
tahun_lahir = 1998
saldo = -50000
nol = 0

# int untuk bilangan yang sangat besar
populasi_dunia = 7800000000
angka_besar = 3273681237101732701376156721

print(type(umur)) #<class 'int'>
print(type(angka_besar)) #<class 'int'>

# float bilangan desimal positif dan negatif
tinggi = 170.5
berat = 50.5
pi = 3.14159
suhu = -10.5

# float untuk notasi scientific
speed_of_light = 3e8 #3 x 10^8
very_small = 1e-6 #1 x 10^-6

print(type(tinggi)) #<class 'float'>
print(type(speed_of_light)) #<class 'float'>

# string dengan single quotes
nama = 'John Doe'
kota = 'Jakarta'

# string dengan double quotes
alamat = "jl. Merdeka"
pesan = "Selamat datang python!"

# string dengan triple quotes
puisi = """
ini adalah puisi
yang terdiri dari
beberapa baris
"""

# string kosong
empty_string = ''
another_empty = ""

print(type(nama)) #<class 'string'>

# boolean
is_student = True
is_married = False
has_license = True

print(type(is_married)) #<class 'bool'>